// Assignment of all primitive types
byte b = 1;
short s = 2;
char c = 'c';
int i = 4;
long l = 5L;
float f = 6.0f;
double d = 7.0d;
boolean bo = true;

// Re-assignments
i = -4;
l = 5;
f = 6;
d = 7;
d = 7.0f;
d = 7.0;
bo = false;
bo = !bo;

